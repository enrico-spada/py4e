# try except
# used when there is a piece of code you know it might fail
# eliminate or catch traceback
astr = "Hello Bob"
istr = int(astr) #blows up if astr is not digits and we get the traceback
#the code would stop here!


astr = "Hello Bob"
try:
    istr = int(astr)
except:   #python executes this code if something goes wrong
    istr = -1

print("First", istr)


